char	*ft_strcat(char *s1, char *s2)
{
	char	*roll1;
	char	*roll2;

	roll1 = s1;
	roll2 = (char *)s2;
	while (*roll1 != '\0')
	{
		roll1++;
	}
	while (*roll2 != '\0')
	{
		*roll1 = *roll2;
		roll1++;
		roll2++;
	}
	*roll1 = '\0';
	return (s1);
}
