char	*ft_strncpy(char *res, char *sym, unsigned int n)
{
	int	i;

	while (i < n && sym[i] != '\0')
	{
		res[i] = sym[i];
		i++;
	}
	while (i < n)
	{
		res[i] = '\0';
		i++;
	}
	return (res);
}
