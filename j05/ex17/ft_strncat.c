char	*ft_strncat(char *dest, char *res, int nb)
{
	char	*cur;

	cur = dest;
	while (*cur != '\0')
	{
		cur++;
	}
	while (*res != '\0' && nb > 0)
	{
		*cur = *res;
		cur++;
		res++;
		nb--;
	}
	*cur = '\0';
	return (dest);
}
